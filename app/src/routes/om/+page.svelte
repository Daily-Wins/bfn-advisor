    <!-- Problemet -->
    <section class="mb-12">
      <h2 class="text-xl font-semibold text-zinc-200 mb-4">Problemet med vanlig AI</h2>

      <p class="text-[15px] text-zinc-400 leading-relaxed mb-4">
        Du har säkert testat att fråga ChatGPT, Copilot eller Claude om en redovisningsfråga.
        Svaret låter bra. Formuleringen är övertygande. Punktnumret ser rimligt ut.
      </p>
      <p class="text-[15px] text-zinc-400 leading-relaxed mb-4">
        Men stämmer det?
      </p>
      <p class="text-[15px] text-zinc-400 leading-relaxed mb-4">
        Oftast inte. Generella AI-verktyg har ett grundläggande problem:
        <span class="text-zinc-200">de arbetar med gammal data</span>.
        Deras kunskap om BFN:s regelverk kan vara månader eller år efter. Gränsvärden ändras.
        Punktnummer stämmer inte. Och det värsta &mdash; AI:n säger aldrig
        <em>"jag vet inte"</em>. Den hittar på ett svar som låter korrekt.
      </p>

      <div class="rounded-xl border border-zinc-800/60 bg-zinc-900/30 p-5 my-6">
        <p class="text-[15px] text-zinc-300 leading-relaxed mb-3 font-medium">
          Ett konkret exempel:
        </p>
        <p class="text-[15px] text-zinc-400 leading-relaxed">
          Fråga ChatGPT om gränsvärdet för direktavdrag av inventarier enligt K2.
          Du får sannolikt ett belopp &mdash; men det kan vara ett gammalt gränsvärde,
          ett påhittat punktnummer, eller en sammanblandning med skatteregler.
          K2K3.ai svarar med rätt belopp och hänvisar till exakt punkt i BFNAR 2016:10.
        </p>
      </div>

      <p class="text-[15px] text-zinc-400 leading-relaxed">
        För en redovisningsekonom är det här inte en kuriosa &mdash;
        det är en <span class="text-zinc-200">riskfråga</span>.
        Fel regeltolkning leder till felaktiga bokslut, revisionsanmärkningar
        och i värsta fall skatterättsliga konsekvenser.
      </p>
    </section>


    <!-- Vår lösning -->
    <section class="mb-12">
      <h2 class="text-xl font-semibold text-zinc-200 mb-4">Hur K2K3.ai löser det</h2>

      <p class="text-[15px] text-zinc-400 leading-relaxed mb-4">
        K2K3.ai fungerar annorlunda. Istället för att förlita sig på vad AI:n "minns"
        från sin träning, arbetar vi med
        <span class="text-zinc-200">BFN:s faktiska regelverk som källa</span>.
      </p>

      <p class="text-[15px] text-zinc-400 leading-relaxed mb-4">
        Så här går det till:
      </p>

      <ol class="space-y-4 text-[15px] text-zinc-400 leading-relaxed">
        <li class="flex gap-3">
          <span class="text-violet-400 font-bold shrink-0">1.</span>
          <span>Du ställer en fråga. AI:n identifierar vilka delar av regelverken som är relevanta.</span>
        </li>
        <li class="flex gap-3">
          <span class="text-violet-400 font-bold shrink-0">2.</span>
          <span>De relevanta kapitlen laddas in &mdash; den faktiska lagtexten, allmänna råden och BFN:s kommentarer.</span>
        </li>
        <li class="flex gap-3">
          <span class="text-violet-400 font-bold shrink-0">3.</span>
          <span>AI:n formulerar svaret baserat <span class="text-zinc-200">enbart</span> på dessa källtexter &mdash; inte på gammal träningsdata.</span>
        </li>
        <li class="flex gap-3">
          <span class="text-violet-400 font-bold shrink-0">4.</span>
          <span>Varje svar visar vilka kapitel och regelverk som använts, så du kan verifiera själv.</span>
        </li>
      </ol>

      <p class="text-[15px] text-zinc-400 leading-relaxed mt-4">
        Om svaret inte finns i regelverket säger K2K3.ai det &mdash; istället för att gissa.
      </p>
    </section>


